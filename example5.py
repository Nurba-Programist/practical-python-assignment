# Начальные значения
start = 1
finish = 100
count = 0

print("Загадай число от 1 до 100 (включительно) и я попробую его угадать.")

# Основной цикл бинарного поиска
while start <= finish:
    count += 1
    n = (start + finish) // 2
    print(f"Попытка {count}: Твоё число равно, меньше или больше {n}?")
    
    # Получение ответа от пользователя
    answer = int(input("Введи 1 - равно, 2 - больше, 3 - меньше: "))
    
    if answer == 1:
        print(f"Ура! Я угадал число {n} с {count}-й попытки!")
        break
    elif answer == 2:
        start = n + 1  # Если число больше, сужаем диапазон сверху
    elif answer == 3:
        finish = n - 1  # Если число меньше, сужаем диапазон снизу
    else:
        print("Некорректный ввод. Попробуй снова.")

# Если цикл завершился и не было найдено числа
if start > finish:
    print("Что-то пошло не так, не могу угадать число.")

